	// precondition: txt is one line, all lowercase
	// takes each letter's ascii value, turns it to an index value in the letters list, and prints out the text.
	public static void printTitle(String txt) {
		txt = txt.toLowerCase();
		for (int line = 0; line < 8; line++) {
			for (int letter = 0; letter < txt.length(); letter++) {
				int pos;
				if ((int) txt.charAt(letter) == 32) //space character
					pos = 26;
				else if ((txt.charAt(letter) - 97) < 27 && (txt.charAt(letter) - 97) >= 0)
					pos = (int) txt.charAt(letter) - 97;
				else {
					pos = 26;
				}
				System.out.print(titleLetters[pos][line]);
			}
			System.out.println("");
		}
		System.out.println("");
	}
	

